import x3dpsail as x3d


X3D0 = (x3d.X3D().setProfile("Immersive").setVersion("3.3")
      .setHead(x3d.head()
        .addMeta(x3d.meta().setName(x3d.SFString("title")).setContent("abox.x3d"))
        .addMeta(x3d.meta().setName(x3d.SFString("creator")).setContent("John Carlson"))
        .addMeta(x3d.meta().setName(x3d.SFString("generator")).setContent("manual"))
        .addMeta(x3d.meta().setName(x3d.SFString("identifier")).setContent("https://coderextreme.net/X3DJSONLD/abox.x3d"))
        .addMeta(x3d.meta().setName(x3d.SFString("description")).setContent("a box")))
      .setScene(x3d.Scene()
        .addChild(x3d.ProtoDeclare().setName(x3d.SFString("anyShape"))
          .setProtoInterface(x3d.ProtoInterface()
            .addField(x3d.field().setName(x3d.SFString("myShape")).setAccessType("inputOutput").setType("MFNode")
              .addChild(x3d.Shape()
                .setGeometry(x3d.Sphere()))))
          .setProtoBody(x3d.ProtoBody()
            .addChild(x3d.Transform()
              .setIS(x3d.IS()
                .addConnect(x3d.connect().setNodeField(x3d.SFString("children")).setProtoField(x3d.SFString("myShape")))))))
        .addChild(x3d.ProtoDeclare().setName(x3d.SFString("one"))
          .setProtoInterface(x3d.ProtoInterface()
            .addField(x3d.field().setName(x3d.SFString("myShape")).setAccessType("inputOutput").setType("MFNode")
              .addChild(x3d.Shape()
                .setGeometry(x3d.Cylinder()))))
          .setProtoBody(x3d.ProtoBody()
            .addChild(x3d.Transform()
              .addChild(x3d.ProtoInstance().setName(x3d.SFString("anyShape"))
                .setIS(x3d.IS()
                  .addConnect(x3d.connect().setNodeField(x3d.SFString("myShape")).setProtoField(x3d.SFString("myShape"))))))))
        .addChild(x3d.ProtoInstance().setName(x3d.SFString("one"))
          .addFieldValue(x3d.fieldValue().setName(x3d.SFString("myShape"))
            .addChild(x3d.Shape()
              .setGeometry(x3d.Box().setSize([140,140,140])))))))

X3D0.toFileX3D("abox_RoundTrip.x3d")
